CREATE TABLE Hotel (
    Hotel_ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Hotel_Name VARCHAR(100) NOT NULL,
    Address VARCHAR(255) NOT NULL,
    City VARCHAR(50) NOT NULL,
    Postal_Code VARCHAR(10),
    Phone_Number VARCHAR(15) NOT NULL,
    Manager_Name VARCHAR(50)
);
CREATE TABLE Guest (
    Guest_ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    First_Name VARCHAR(50) NOT NULL,
    Last_Name VARCHAR(50) NOT NULL,
    National_ID VARCHAR(17) UNIQUE,
    Phone_Number VARCHAR(15) NOT NULL,
    Email VARCHAR(100),
    Address VARCHAR(255)
);
CREATE TABLE Room (
    Room_ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Hotel_ID INT NOT NULL,
    Room_Number VARCHAR(10) NOT NULL,
    Room_Type VARCHAR(20) NOT NULL,
    Price_Per_Night DECIMAL(10,2) NOT NULL,
    Availability_Status VARCHAR(20) DEFAULT 'Available',
    CONSTRAINT fk_hotel FOREIGN KEY (Hotel_ID) REFERENCES Hotel(Hotel_ID)
);
CREATE TABLE Booking (
    Booking_ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Guest_ID INT NOT NULL,
    Room_ID INT NOT NULL,
    Booking_Date DATE NOT NULL,
    Check_In_Date DATE NOT NULL,
    Check_Out_Date DATE NOT NULL,
    Total_Amount DECIMAL(10,2) NOT NULL,
    CONSTRAINT fk_guest FOREIGN KEY (Guest_ID) REFERENCES Guest(Guest_ID),
    CONSTRAINT fk_room FOREIGN KEY (Room_ID) REFERENCES Room(Room_ID)
);
CREATE TABLE Staff (
    Staff_ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Hotel_ID INT NOT NULL,
    Staff_Name VARCHAR(50) NOT NULL,
    Role VARCHAR(50) NOT NULL,
    Salary DECIMAL(10,2) NOT NULL,
    Joining_Date DATE NOT NULL,
    CONSTRAINT fk_staff_hotel FOREIGN KEY (Hotel_ID) REFERENCES Hotel(Hotel_ID)
);
